    private void jb_olusturActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_olusturActionPerformed
        // TODO add your handling code here:
        String durum = "open";
        baslık = jt_bas.getText();
        tanım = jt_tan.getText();

        if (calısan != null && baslık.length() != 0 && tanım.length() != 0) {
            sorguu.mesaj_ekle(baslık, tanım, calısan, durum, (String) jc_seviye.getSelectedItem(), Sorgu.id);
           
            int deger = sorguu.count();
            if (deger > sayi) {
                jl_hata.setText("Defect Oluşturuldu");
                jl_hata2.setText("");
            }

        } else if (calısan == null) {
            jl_hata.setText("Defect Oluşturulamadı");
            jl_hata2.setText("Assignee seçin");

        } else if (baslık.equals("")) {
            jl_hata.setText("Defect Oluşturulamadı");
            jl_hata2.setText("Başlığı doldurun");

        } else if (tanım.equals("")) {
            jl_hata.setText("Defect Oluşturulamadı");
            jl_hata2.setText("Tanımı doldurun");

        }

        seviye = (String) jc_seviye.getSelectedItem();


    }//GEN-LAST:event_jb_olusturActionPerformed

