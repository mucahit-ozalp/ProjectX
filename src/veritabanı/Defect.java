    public void listele(int d_deger, int c_deger) {

        ArrayList<String> liste = new ArrayList<String>();
        String sorgu = "SELECT kd.kullanici_adi from kullanici_db kd, departman_db dd WHERE kd.dk_id=dd.d_id AND dd.departman=? AND dd.companent=? ";

        try {
            PreparedStatement preparedStatement = sorguu.b.Baglan().prepareStatement(sorgu);
            preparedStatement.setInt(1, d_deger);
            preparedStatement.setInt(2, c_deger);
            ResultSet rs = preparedStatement.executeQuery();

            System.out.println("afdsf");

            while (rs.next()) {

                liste.add(rs.getString("kullanici_adi"));
                System.out.println("liste :" + liste);

            }
            String dizi[] = new String[liste.size()];
            for (int i = 0; i < liste.size(); i++) {
                dizi[i] = liste.get(i);

                // jc_assi.setActionCommand((String) sorguu.listele().get(i));
                System.out.println("boyut :" + liste.size());
                System.out.println("ilk :" + liste.get(i));
            }
            jc_assi.setModel(new DefaultComboBoxModel<>(dizi));

        } catch (SQLException ex) {

        }

    }

